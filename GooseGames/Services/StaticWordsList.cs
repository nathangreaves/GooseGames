        public static List<string> GetWords(int numberOfWords, IEnumerable<WordListEnum> includedWordLists, int? wordLength = null)
        {
            List<string> fullList = new List<string>();

            foreach (var list in includedWordLists)
            {
                switch(list)
                {
                    case WordListEnum.JustOne:
                        fullList.AddRange(JustOne);
                        break;
                    case WordListEnum.Codenames:
                        fullList.AddRange(Codenames);
                        break;
                    case WordListEnum.CodenamesDuet:
                        fullList.AddRange(CodenamesDuet);
                        break;
                    case WordListEnum.CodenamesDeepUndercover:
                        fullList.AddRange(CodenamesDeepUndercover);
                        break;
                }
            }

            //This distinct here is important as without it, the probability of picking words which are duplicated across distinct sessions is higher than words which are not
            List<string> allWords = fullList.Select(x => x.ToLower()).Distinct().ToList();
            var numberOfWordsTotal = allWords.Count;
            if (numberOfWords > numberOfWordsTotal)
            {
                throw new ArgumentOutOfRangeException(nameof(numberOfWords));
            }

            var random = new Random();
            var shuffledList = allWords.OrderBy(x => random.Next()).ToList();

            var results = new List<string>();

            while (results.Count < numberOfWords)
            {
                string item = shuffledList[random.Next(numberOfWordsTotal)].ToUpper();

                if (!results.Contains(item) && (!wordLength.HasValue || item.Length == wordLength.Value))
                {
                    results.Add(item);
                }
            }

            return results;
        }

